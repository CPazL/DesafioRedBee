public class SumTwoNumbers {
        int a;
        int b;
       
        public void sumTwoNumbers(){
            
        }
    
        
    public int sum(int a, int b){
        this.a=a;
        this.b=b;
        
        return a+b;
    }
    public void SetA(int a){
        this.a=a;
    }
    public void setB(int b){
        this.b=b;
    }
    public int getA(){
        return a;
    }
    public int getB(){
        return b;
    }
}
